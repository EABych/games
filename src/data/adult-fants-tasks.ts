// Функция получения случайного задания
export const getRandomAdultFantsTask = (): AdultFantsTask => {
  const randomIndex = Math.floor(Math.random() * adultFantsTasks.length);
  return adultFantsTasks[randomIndex];
};

// Функция получения задания по сложности
export const getTaskByDifficulty = (difficulty: 'easy' | 'medium' | 'hard' | 'mixed'): AdultFantsTask => {
  if (difficulty === 'mixed') {
    return getRandomAdultFantsTask();
  }
  
  const filteredTasks = adultFantsTasks.filter(task => task.difficulty === difficulty);
  const randomIndex = Math.floor(Math.random() * filteredTasks.length);
  return filteredTasks[randomIndex];
};

// Функция получения задания по категории
export const getTaskByCategory = (category: string): AdultFantsTask => {
  const filteredTasks = adultFantsTasks.filter(task => task.category === category);
  const randomIndex = Math.floor(Math.random() * filteredTasks.length);
  return filteredTasks[randomIndex];
};