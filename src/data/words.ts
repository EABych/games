export function getRandomWord(difficulty: DifficultyLevel = 'easy'): string {
  const words = getWordsByDifficulty(difficulty);
  return words[Math.floor(Math.random() * words.length)];
}

export function getAllWords(): string[] {
  return [...wordsDatabase.easy, ...wordsDatabase.medium, ...wordsDatabase.hard];
}

export function getTotalWordsCount(): number {
  return getAllWords().length;
}

export function getWordsCountByDifficulty(difficulty: DifficultyLevel): number {
  return wordsDatabase[difficulty].length;
}

